/*
Student Grading Application
----------------------------
This C# console application calculates and displays final grades for students based on their exam and extra credit scores.
Each student has 5 exam scores (0–100), and optional extra credit assignments.
Extra credit scores (if present) are identified by array length and weighted at 10% of a full exam score.
The program calculates the average score, applies extra credit, and assigns a letter grade automatically.
Uses arrays and foreach loops to handle students and scores for scalability and easier maintenance.
Outputs each student’s name, final numeric grade, and corresponding letter grade in a formatted report.
*/