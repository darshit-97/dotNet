Console.WriteLine(input1);
Console.WriteLine(ReverseSentence(input1));

string ReverseSentence(string input1) 
{
    string result = "";
    string[] words = input1.Split(" ");

    foreach(string word in words) 
    {
        result += ReverseWord(word) + " ";
    }

    return result.Trim();
}